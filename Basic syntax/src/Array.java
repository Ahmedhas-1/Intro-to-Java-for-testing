public class Array {
    public static void main(String[] args) {
        // declaration
        int iArray1[] = new int[4];

        // initialization
        iArray1[0] = 32;
        iArray1[1] = 13;
        iArray1[2] = 56;
        iArray1[3] = 78;

        int[] a = { 25, 50, 75, 100 };
        System.out.println(a[0]);
        System.out.println(a[1]);
        System.out.println(a[2]);
        System.out.println(a[3]);

        // declaration
        int matrix[][] = new int[2][2];

        // initialization
        matrix[0][0] = 1;
        matrix[0][1] = 2;
        matrix[1][0] = 3;
        matrix[1][1] = 4;
    }
}
